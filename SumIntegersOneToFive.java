/**
 * This loop calculates the sum of the integers from 1 to 5 using a for loop.
 *
 * Mario Montano
 * 
 */
public class SumIntegersOneToFive
{
    public static void main( String [ ] args )
    {
        int sum = 0;
        for( int i = 0; i <= 5; i++ )
        {
            sum += i;
        }
        System.out.println( sum );
    }
}
